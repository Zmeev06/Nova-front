export const count = (days: number, hours: number) => {
  if (days === 4 && hours === 6)
    return '96.000'
  else if (days === 5 && hours === 6)
    return '120.000'
  else if (days === 6 && hours === 6)
    return '144.000'
  else if (days === 4 && hours === 7)
    return '140.000'
  else if (days === 5 && hours === 7)
    return '175.000'
  else if (days === 6 && hours === 7)
    return '210.000'
  else if (days === 4 && hours === 8)
    return '180.000'
  else if (days === 5 && hours === 8)
    return '225.000'
  else if (days === 6 && hours === 8)
    return '270.000'
  else if (days === 4 && hours === 9)
    return '200.000'
  else if (days === 5 && hours === 9)
    return '250.000'
  else if (days === 6 && hours === 9)
    return '300.000'
}
